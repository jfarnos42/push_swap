#include "libft.h"

int	ft_atoi(const char *c)
{
	int	i;
	int	sign;
	int	res;

	i = 0;
	sign = 1;
	res = 0;
	while (c[i] == ' ' || (c[i] >= 9 && c[i] <= 13))
		i++;
	if (c[i] == '-' || c[i] == '+')
	{
		if (c[i++] == '-')
			sign *= -1;
	}
	while (c[i] >= '0' && c[i] <= '9')
		res = res * 10 + (c[i++] - '0');
	return (res * sign);
}
