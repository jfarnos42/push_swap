#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		s1len;
	int		i;

	s1len = ft_strlen(s1);
	s2 = (char *)malloc(sizeof(char) * (s1len + 1));
	if (s2 == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
